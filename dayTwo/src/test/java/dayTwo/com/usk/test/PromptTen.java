package dayTwo.com.usk.test;

public class PromptTen {

	public static void main(String[] args) {
		// Iterate for loop from 1 to 100
		//using continue statement
		//print only odd numbers
		
		for (int i=1; i<=100; i++)
		{
			
			if(i%2==0) {
			
			continue;
			}
			
			System.out.println(i+"");
		}

	}
	}

